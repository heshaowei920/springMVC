public class Role {
    private Long theID;
    private String roleName;
    

	public Long getTheID() {
		return theID;
	}
	public void setTheID(Long theID) {
		this.theID = theID;
	}
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
    
    
}
